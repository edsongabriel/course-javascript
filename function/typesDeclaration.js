// function declaration
function soma(x, y) {
    return x + y;
}

// function expression
const sub = function (x, y) {
    return x - y;
}// observação: este tipo de declaração só pode ser chamada depois que for definida, caso contrário, a função não será reconhecida.

// named function expression
const mult = function mult(x, y) {
    return x * y;
    
}// observação: este tipo de declaração só pode ser chamada depois que for definida, caso contrário, a função não será reconhecida.